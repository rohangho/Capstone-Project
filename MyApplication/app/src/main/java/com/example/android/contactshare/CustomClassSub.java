public class CustomClassSub {

    private int ImageId;
    private String Subject;

    public CustomClassSub(int ImageId, String Subject) {
        this.ImageId = ImageId;
        this.Subject = Subject;
    }

    public int getImageId() {
        return ImageId;
    }

    public String getSubject() {
        return Subject;
    }


}
