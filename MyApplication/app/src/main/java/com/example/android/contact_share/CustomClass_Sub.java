public class CustomClass_Sub {

    private int ImageId;
    private String Subject;

    public CustomClass_Sub(int ImageId, String Subject) {
        this.ImageId = ImageId;
        this.Subject = Subject;
    }

    public int getImageId() {
        return ImageId;
    }

    public String getSubject() {
        return Subject;
    }


}
